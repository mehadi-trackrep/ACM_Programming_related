struct edge
{
    int u,v,w;
    bool operator < (const edge &p)const
    {
        return (w < p.w); /// jetar upor basis kore sort hobe.../ means small weight(w) gula first a rakhbe...
    }
};

struct new_edge
{
    int u,v,w;
};

vector <edge> e;
vector <new_edge> tree;

void makeset()
{
    for(int i=0; i<=mxn; i++)
        par[i] = i;
}

int find_set(int r)
{
    if(r == par[r])
        return r;
    return par[r] = find_set(par[r]);
}

void MST(int N)
{
    sort(e.begin(),e.end());
    makeset();
    int cnt = 0;
    int cost = 0;

    for(int i=0; i<e.size(); i++)
    {
        int u,v,w;
        u = e[i].u;
        v = e[i].v;
        w = e[i].w;
        //union_check(u,v,w);
        int x,y;
        x = find_set(u);
        y = find_set(v);
        if(x != y){
            cnt++;
            new_edge get;
            get.u = u;
            get.v = v;
            get.w = w;
            tree.push_back(get);   /// track for tree
            par[x] = y;
            cost += w;
            if(cnt == N-1)
                break;
        }
    }
}

