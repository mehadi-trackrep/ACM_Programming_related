void bfs(int src)       ///AC
{
    int u , i , v ;
    queue<int>q ;
    q.push(src);
    int visited[SIZ] = {0} , level[SIZ];
    visited[src] = 1;
    level[src] = 0;

    parent[src] = src;

    while(!q.empty())
    {
        u = q.front();
        for(i=0 ; i<graph[u].size() ; i++)
        {
            v = graph[u][i] ;

            if(!visited[v])
            {
                level[v] = level[u] + 1 ;
                parent[v] = u ;
                visited[v] =  1 ;
                q.push(v) ;
                if(level[v] > max_level)
                {
                    node = v;
                    max_level = level[v];
                }
            }
        }
        q.pop();
    }

}

