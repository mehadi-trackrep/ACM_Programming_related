int longest(int u)
{
    if(dp[u] != -1) return dp[u];
    int maxi = 0;
    for(int v = u+1; v <= n; v++) // ist condition ,v>u
    {
        if(value[v].second < value[u].second) // 2nd condition , value[v]>value[u]
        {
            if(longest(v) > maxi ) // maximum value ta nibo ...
            {
                maxi = longest(v);
                dir[u] = v;
            }
        }
    }
    dp[u] = 1 + maxi ; // u er longest path
    return dp[u];
}

