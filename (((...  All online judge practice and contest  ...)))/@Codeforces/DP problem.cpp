int rec(int total,int pos)
{
    if(total == n)
    {
        cerr << "\n==>\n";
        return 0;
    }
    if(dp[pos] != -1)
    {
        cerr << "-->\n";
        return dp[pos];
    }
    int ret = 0;
    for(int i=pos; i<n; i++)
    {
        int ret1,ret2;
        ret1 = price[i]*(total+1) + rec(total+1,i);
        ret2 = price[i]*(total+1) + rec(total+1,n-i-1);
        ret = max(ret,(ret1,ret2));
    }
    return dp[pos] = ret;
}

