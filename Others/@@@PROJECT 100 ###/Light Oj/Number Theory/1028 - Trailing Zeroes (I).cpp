int prime[78500],vis[SIZ];
int p_cnt = 0;

void prime_seive()
{
    prime[p_cnt++] = 2;
    memset(vis,0,sizeof(vis));
    for(int i=3; i<SIZ; i+=2)
    {
        if(!vis[i])
        {
            prime[p_cnt++] = i;
            for(int j=i+i; j<SIZ; j+=i) vis[j] = 1;
        }
    }
}

ll NOD_for_trailing_Zeros(ll n)
{
    ll ans=1;
    for(int i=0; (ll)prime[i]*prime[i]<=n; i++)
    {
        if(!(n%prime[i]))
        {
            ll freq = 1;
            while(!(n%prime[i]))
            {
                n /= prime[i];
                freq++;
            }
            ans *= freq;
        }
    }
    if(n!=1) ans *= 2;      /// if n = p1^a1 .* p2^a2 then NOD = (a1+1)*(a2+1);
    return --ans;     /// ekhettere 1 kono number system na , so ans--;
}

